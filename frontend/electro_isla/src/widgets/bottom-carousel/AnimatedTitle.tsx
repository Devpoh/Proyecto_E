import { useEffect, useRef, useState } from 'react';
import './AnimatedTitle.css';

interface AnimatedTitleProps {
  text: string;
}

export const AnimatedTitle = ({ text }: AnimatedTitleProps) => {
  const containerRef = useRef<HTMLDivElement>(null);
  const titleRef = useRef<HTMLHeadingElement>(null);
  const underlineRef = useRef<HTMLDivElement>(null);
  const [isVisible, setIsVisible] = useState(false);

  useEffect(() => {
    const observer = new IntersectionObserver(
      ([entry]) => {
        if (entry.isIntersecting) {
          setIsVisible(true);
          observer.unobserve(entry.target);
        }
      },
      {
        threshold: 0.5,
      }
    );

    if (containerRef.current) {
      observer.observe(containerRef.current);
    }

    return () => {
      if (containerRef.current) {
        observer.unobserve(containerRef.current);
      }
    };
  }, []);

  // ‚úÖ Ajustar ancho de la l√≠nea al ancho del t√≠tulo (optimizado para CLS)
  useEffect(() => {
    const updateWidth = () => {
      if (titleRef.current && underlineRef.current) {
        const titleWidth = titleRef.current.offsetWidth;
        underlineRef.current.style.setProperty('--underline-width', `${titleWidth}px`);
      }
    };

    // Ejecutar inmediatamente
    updateWidth();

    // Tambi√©n ejecutar despu√©s de que las fuentes carguen
    if (document.fonts) {
      document.fonts.ready.then(updateWidth);
    }
  }, [text]);

  return (
    <div className="titulo-container" ref={containerRef}>
      <h1 className="titulo" ref={titleRef}>{text}</h1>
      <div className={`titulo-underline ${isVisible ? 'active' : ''}`} ref={underlineRef}></div>
    </div>
  );
};
